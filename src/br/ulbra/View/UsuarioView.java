    private void tblUsuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuMouseClicked
         int row = tblUsu.getSelectedRow();
        if (row >= 0) {
            txtID.setText(tblUsu.getValueAt(row, 0).toString());
            txtNome.setText(tblUsu.getValueAt(row, 1).toString());
            txtLogin.setText(tblUsu.getValueAt(row, 2) != null ? tblUsu.getValueAt(row, 2).toString() : "");
            btnAtivo.setText(tblUsu.getValueAt(row, 3) != null ? tblUsu.getValueAt(row, 3).toString() : "");
        }
        setBotoes(1);
    }//GEN-LAST:event_tblUsuMouseClicked

