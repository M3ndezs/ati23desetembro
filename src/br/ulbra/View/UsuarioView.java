    /*Método setBotoes */
    public void setBotoes(int op) {
        switch (op) {
            case 1:
                btnSalvar.setEnabled(false);
                btnAlterar.setEnabled(true);
                btnExcluir.setEnabled(true);
                break;
            default:
                btnSalvar.setEnabled(true);
                btnAlterar.setEnabled(false);
                btnExcluir.setEnabled(false);

        }
    }

    private void initTableModel() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{"id", "nome", "login", "senha", "ativo"}) {
            @Override
            public boolean isCellEditable(int rol, int col) {
                return false;
            }
        };
        tblUsu.setModel(model);
    }

    private void atualizarTable() {
        try {
            DefaultTableModel modelTbl = (DefaultTableModel) tblUsu.getModel();
            modelTbl.setRowCount(0);
            List<Usuario> lista = controller.listar();
            for (Usuario u : lista) {
                modelTbl.addRow(new Object[]{
                    u.getId(), u.getNome(), u.getLogin(), u.getSenha(), u.isAtivo()
                });
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "ERRO AO LISTAR" + ex.getMessage());
        }
    }

    public void limparCampos() {
        txtID.setText(null);
        txtNome.setText(null);
        txtLogin.setText(null);
        txtSenha.setText(null);
        btnAtivo.setText("");
    }

